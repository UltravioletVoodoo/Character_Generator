export class race {
    constructor(name, abilityScores, speed, size, traits, ageRange, languages) {
        this.name = name;
        this.abilityScores = abilityScores;
        this.speed = speed;
        this.size = size;
        this.traits = traits;
        this.ageRange = ageRange;
        this.languages = languages;
    }
}
